#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		len;

	len = ft_strlen(s1);

	s2 = (char *)malloc(len * sizeof(char) + 1);
	if (s2)
	{
		s2[len + 1] = '\0';
		return (s2);
	}
}
