#include <unistd.h>

int    ft_putchar(char c)
{
	write(1, &c, 1);
	return(0);
}

void    ft_putnbr(int n)
{
	if (n >= 10)	
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(48 + n);
}

